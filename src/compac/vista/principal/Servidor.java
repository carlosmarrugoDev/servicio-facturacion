    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Encripar encrip = new Encripar();
        archivo.Eliminar_archivo(ruta);
        String basedatos = txtBaseDatos.getText();
        String usuario = txtUsuario.getText();
        String password = txtPassword.getText();
        String servidor = txtServidor.getText();
        String puerto = txtPuerto.getText();

        String dato = encrip.Encriptar(basedatos) + ";" + encrip.Encriptar(usuario) + ";" + encrip.Encriptar(password) + ";" + encrip.Encriptar(servidor) + ";" + encrip.Encriptar(puerto);
        archivo.crearTXT(ruta, dato);
        txtBaseDatos.setText("");
        txtUsuario.setText("");
        txtPassword.setText("");
        txtServidor.setText("");
        this.dispose();

        Ini_Presentacion pres = new Ini_Presentacion();
        pres.setLocationRelativeTo(null);
        pres.setVisible(true);
        /// EXPORTAR A EXEL UNA ALERTA 
        Alerta alerta = new Alerta("Mensaje", "Ejecute Nuevamente el Sistema");
        alerta.setVisible(true);


    }//GEN-LAST:event_jButton1ActionPerformed

